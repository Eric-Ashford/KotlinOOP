//Nested classes are a class inside of another class

class Outer{
    private val name:String? = null

    inner class iNested{
        fun show():String{
            return "From inner nested"
        }
    }

    class Nested{
        fun show():String {
            return "From nested"
        }
    }

}

fun main(args:Array<String>){

    var outer = Outer().iNested().show()
    var inner = Outer.Nested()

    println(outer)
    println()
    println(inner.show())

}

